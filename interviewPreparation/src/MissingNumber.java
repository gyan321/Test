
public class MissingNumber {

	public static void main(String[] args) {

		int missingInteger = 0;

		int[] values = {1, 2, 4, 5, 6, 7, 8, 9, 10};

		for (int i = 0; i < values.length; i++) {
			if (values[i]-i!=1) {
			 
				missingInteger = values[i]-1;
				System.out.println("misssing value" + missingInteger);
				break;
			}

		}
		if (missingInteger == 0) {
			System.out.println("no missing value");
		}
	}

}
